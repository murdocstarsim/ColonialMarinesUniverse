    private void RebuildTabs()
    {
        _draggingTabId = null;
        _dragTargetTabId = null;

        foreach (var button in _tabButtons.Values)
        {
            TabBar.RemoveChild(button);
        }

        _tabButtons.Clear();

        foreach (var tab in _tabs)
        {
            if (!IsTabVisible(tab))
                continue;

            var capturedTabId = tab.Id;
            var isAll = IsAllTab(tab);
            var button = new ChatTabButton(tab.Id)
            {
                ToggleMode = true,
                Mode = BaseButton.ActionMode.Release,
                MinWidth = Math.Max(58, tab.Title.Length * 9),
                StyleClasses = { StyleNano.StyleClassChatChannelSelectorButton },
                CanDrag = !isAll
            };
            button.OnPressed += _ => SetActiveTab(capturedTabId);
            button.DragStarted += OnTabDragStarted;
            button.DragEntered += OnTabDragEntered;
            button.DragExited += OnTabDragExited;
            button.DragEnded += OnTabDragEnded;
            TabBar.AddChild(button);
            _tabButtons[capturedTabId] = button;
        }

        if (_tabButtons.Count == 0)
        {
            _tabs = ChatUserSettings.CreateDefaultTabs();
            SaveTabs();
            RebuildTabs();
            return;
        }

        if (!_tabButtons.ContainsKey(_activeTabId))
            _activeTabId = _tabButtons.Keys.First();

        NormalizeSecondaryTab();

        UpdateTabButtons();
        SyncSplitControls();
    }

    private void SetActiveTab(string tabId, bool repopulate = true)
    {
        if (!_tabButtons.ContainsKey(tabId))
            tabId = _tabButtons.Keys.FirstOrDefault() ?? ChatUserSettings.AllTabId;

        _activeTabId = tabId;
        _tabUnread[tabId] = 0;
        UpdateTabButtons();
        SyncFilterPopup();

        if (repopulate)
            Repopulate();
    }

    private void SetSplitChatEnabled(bool enabled, bool repopulate = true, bool save = true)
    {
        enabled &= _splitAvailable;
        _splitChatEnabled = enabled;
        SecondaryPane.Visible = enabled && !LegacyPresentation;

        if (enabled)
        {
            NormalizeSecondaryTab();

            ApplySplitRatio();
            if (repopulate)
                RepopulateSecondary();
        }
        else
        {
            SecondaryContents.Clear();
            _secondaryRepeatQueue.Clear();
        }

        UpdateTabButtons();
        SyncSplitControls();

        if (save)
            SaveSplitSettings();
    }

    private void SetSecondaryTab(string tabId, bool repopulate = true)
