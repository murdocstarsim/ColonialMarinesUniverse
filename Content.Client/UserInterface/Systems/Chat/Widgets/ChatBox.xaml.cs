    {
        var visibleTabs = _tabs.Where(IsTabVisible).ToList();
        SplitButton.Visible = _splitAvailable && !LegacyPresentation;
        SplitButton.Popup.ConfigureTabs(visibleTabs, _secondaryTabId, _splitChatEnabled);
        SplitButton.SetSplitState(_splitChatEnabled, GetTabTitle(_secondaryTabId));
        UpdateTabOverflowControls();
    }

