        if (!IsSecondaryTabVisible(tabId))
            tabId = GetDefaultSecondaryTabId();

        _secondaryTabId = tabId;
        _tabUnread[tabId] = 0;
        UpdateTabButtons();
        SyncSplitControls();
        SaveSplitSettings();

        if (repopulate)
            RepopulateSecondary();
    }

