    {
        var legacy = LegacyPresentation;
        TabHeader.Visible = !legacy;
        ChatPanes.Visible = !legacy;
        LegacyContents.Visible = legacy;
        SecondaryPane.Visible = !legacy && _splitChatEnabled;
        ChatInput.SetLegacyMode(legacy);
        SyncSplitControls();
        SyncFilterPopup();
    }

    private void RepopulateSecondary()
    {
        SecondaryContents.Clear();
        _secondaryRepeatQueue.Clear();

        if (!_splitChatEnabled)
            return;

        var tab = GetSecondaryTab();
        foreach (var message in _controller.History)
        {
            if (IsMessageVisibleInTab(tab, message.Item2))
                AddLine(message.Item2, SecondaryContents, _secondaryRepeatQueue);
        }

        SecondaryContents.ScrollToBottom();
    }

    private void UpdateTabButtons()
    {
        foreach (var (tabId, button) in _tabButtons)
        {
            button.Text = GetTabButtonText(tabId);
            button.Pressed = tabId == _activeTabId;
            button.Modulate = tabId == _activeTabId
                ? Color.FromHex("#9fd0b3")
                : Color.FromHex("#737987");
        }

        UpdateTabDragVisuals();
        UpdateTabOverflowControls();
    }

    private void UpdateTabOverflowControls()
    {
        if (LegacyPresentation || _tabButtons.Count == 0)
        {
            TabOverflowButton.SetHiddenCount(0);
            foreach (var button in _tabButtons.Values)
            {
                button.Visible = true;
            }
            return;
        }

        var visibleTabs = _tabs.Where(tab => IsTabVisible(tab) && _tabButtons.ContainsKey(tab.Id)).ToList();
        _overflowTabs.Clear();

        var width = TabHeader.Width > 0 ? TabHeader.Width : Width;
        if (width <= 0)
        {
            TabOverflowButton.SetHiddenCount(0);
            foreach (var tab in visibleTabs)
            {
                _tabButtons[tab.Id].Visible = true;
            }
            return;
        }

        var splitWidth = SplitButton.Visible
            ? Math.Max(SplitButton.Width, SplitButton.MinWidth) + 4
            : 0;
        var availableWithOverflow = Math.Max(120, width - splitWidth - 52);
        var hidden = ApplyTabOverflow(visibleTabs, availableWithOverflow);

        if (hidden.Count == 0)
            hidden = ApplyTabOverflow(visibleTabs, Math.Max(120, width - splitWidth - 8));

        _overflowTabs.AddRange(hidden);
        TabOverflowButton.SetHiddenCount(_overflowTabs.Count);
        TabOverflowButton.Popup.ConfigureTabs(_overflowTabs, _activeTabId);
    }

    private List<ChatTabSettings> ApplyTabOverflow(IReadOnlyList<ChatTabSettings> visibleTabs, float availableWidth)
    {
        var hidden = new List<ChatTabSettings>();
        var used = 0f;
        var activeReserved = false;

        foreach (var tab in visibleTabs)
        {
            var button = _tabButtons[tab.Id];
            button.Visible = false;
        }

        var active = visibleTabs.FirstOrDefault(tab => string.Equals(tab.Id, _activeTabId, StringComparison.OrdinalIgnoreCase));
        if (active != null)
        {
            var activeButton = _tabButtons[active.Id];
            activeButton.Visible = true;
            used += GetTabButtonWidth(activeButton);
            activeReserved = true;
        }

        foreach (var tab in visibleTabs)
        {
            if (activeReserved && string.Equals(tab.Id, _activeTabId, StringComparison.OrdinalIgnoreCase))
                continue;

            var button = _tabButtons[tab.Id];
            var width = GetTabButtonWidth(button);
            var next = used <= 0 ? width : used + 2 + width;
            if (next <= availableWidth || used <= 0)
            {
                button.Visible = true;
                used = next;
                continue;
            }

            hidden.Add(tab);
        }

        return hidden;
    }

    private static float GetTabButtonWidth(Button button)
    {
        return Math.Max(button.MinWidth, (button.Text?.Length ?? 0) * 9 + 18);
    }

    private string GetTabButtonText(string tabId)
    {
        var title = GetTabTitle(tabId);
        if (_tabUnread.GetValueOrDefault(tabId) is var unread && unread > 0)
            title += unread > 9 ? " (9+)" : $" ({unread})";

        return title;
    }

    private string GetTabTitle(string tabId)
    {
        return _tabs.FirstOrDefault(tab => tab.Id == tabId)?.Title ?? "TAB";
    }

    private void SyncFilterPopup()
    {
        var activeTab = GetActiveTab();
        var available = _controller.FilterableChannels;

        _syncingFilter = true;
        ChatInput.FilterButton.SetAllowedChannels(available);
        ChatInput.FilterButton.Popup.SetActiveMask(
            LegacyPresentation
                ? _legacyChannelMask & available
                : GetTabChannelMask(activeTab) & available,
            quiet: true);
        ChatInput.FilterButton.Popup.SetFiltersReadOnly(!LegacyPresentation && IsAllTab(activeTab));
        ChatInput.FilterButton.Popup.ConfigureRadioFilters(
            _radioTargets,
            LegacyPresentation || IsAllTab(activeTab) ? Array.Empty<string>() : activeTab.RadioLabels,
            !LegacyPresentation && !IsAllTab(activeTab) && (activeTab.ChannelMask & ChatChannel.Radio) != 0);
        ChatInput.FilterButton.Popup.ConfigureTabs(_tabs.Where(IsTabVisible).ToList(), _activeTabId);
        ChatInput.FilterButton.Popup.ConfigureStyles(_styles, _styleTargets);
        ChatInput.FilterButton.Popup.SetColorWholeMessage(_colorWholeMessage);
        ChatInput.FilterButton.Popup.SetLegacyMode(LegacyPresentation);
        _syncingFilter = false;
    }

    private bool IsTabVisible(ChatTabSettings tab)
    {
        if (tab.Id == ChatUserSettings.AdminTabId)
            return (_controller.FilterableChannels & ChatChannel.AdminRelated) != 0;

        return true;
    }

    private void NormalizeSecondaryTab()
    {
        if (!IsSecondaryTabVisible(_secondaryTabId))
            _secondaryTabId = GetDefaultSecondaryTabId();
    }

    private bool IsSecondaryTabVisible(string tabId)
    {
        return _tabs.Any(tab => tab.Id == tabId && IsTabVisible(tab));
    }

    private bool IsMessageVisibleInActiveTab(ChatMessage message)
    {
        return IsMessageVisibleInTab(GetActiveTab(), message);
    }

    private bool IsMessageVisibleInLegacy(ChatMessage message)
    {
        return ((_legacyChannelMask & _controller.FilterableChannels) & message.Channel) != 0;
    }

    private bool IsMessageVisibleInSecondaryTab(ChatMessage message)
    {
        return !LegacyPresentation && _splitAvailable && _splitChatEnabled && IsMessageVisibleInTab(GetSecondaryTab(), message);
    }

    private bool IsMessageVisibleInTab(ChatTabSettings tab, ChatMessage message)
    {
        if (((GetTabChannelMask(tab) & _controller.FilterableChannels) & message.Channel) == 0)
            return false;

        if (IsAllTab(tab) || message.Channel != ChatChannel.Radio || tab.RadioLabels.Count == 0)
            return true;

        var label = ChatUserSettings.NormalizeRadioLabel(message.Display?.ChannelLabel);
        return !string.IsNullOrWhiteSpace(label) &&
               tab.RadioLabels.Any(radioLabel => string.Equals(radioLabel, label, StringComparison.OrdinalIgnoreCase));
    }

    private bool IsMessageVisibleInAnyPage(ChatMessage message)
    {
        foreach (var tab in _tabs)
        {
            if (!IsTabVisible(tab))
                continue;

            if (IsMessageVisibleInTab(tab, message))
                return true;
        }

        return false;
    }

    private void UpdateInactiveTabUnreads(ChatMessage message)
    {
        foreach (var tab in _tabs)
        {
            if (IsTabOpen(tab.Id) || !IsTabVisible(tab))
                continue;

            if (!IsMessageVisibleInTab(tab, message))
                continue;

            _tabUnread[tab.Id] = _tabUnread.GetValueOrDefault(tab.Id) + 1;
        }

        UpdateTabButtons();
    }

    private bool IsTabOpen(string tabId)
    {
        return string.Equals(tabId, _activeTabId, StringComparison.OrdinalIgnoreCase) ||
               !LegacyPresentation && _splitChatEnabled && string.Equals(tabId, _secondaryTabId, StringComparison.OrdinalIgnoreCase);
    }

    private ChatTabSettings GetActiveTab()
    {
        var active = _tabs.FirstOrDefault(tab => tab.Id == _activeTabId);
        if (active != null)
            return active;

        if (_tabs.Count == 0)
            _tabs = ChatUserSettings.CreateDefaultTabs();

        _activeTabId = _tabs[0].Id;
        return _tabs[0];
    }

    private ChatTabSettings GetSecondaryTab()
    {
        var secondary = _tabs.FirstOrDefault(tab => tab.Id == _secondaryTabId && IsTabVisible(tab));
        if (secondary != null)
            return secondary;

        _secondaryTabId = GetDefaultSecondaryTabId();
        return _tabs.FirstOrDefault(tab => tab.Id == _secondaryTabId) ?? GetActiveTab();
    }

    private string GetDefaultSecondaryTabId()
    {
        var radio = _tabs.FirstOrDefault(tab => tab.Id == ChatUserSettings.RadioTabId && IsTabVisible(tab));
        if (radio != null)
            return radio.Id;

        return _tabs.FirstOrDefault(IsTabVisible)?.Id ?? ChatUserSettings.AllTabId;
    }

    private void OnSettingsTabSelected(string tabId)
    {
        SetActiveTab(tabId);
    }

    private void OnSettingsTabAdded(string title)
    {
        var tab = new ChatTabSettings
        {
            Id = ChatUserSettings.CreateCustomTabId(),
            Title = ChatUserSettings.SanitizeTabTitle(title),
            ChannelMask = _controller.FilterableChannels == ChatChannel.None
                ? ChatChannel.OOC
                : _controller.FilterableChannels,
            RadioLabels = new List<string>()
        };

        _tabs.Add(tab);
        SaveTabs();
        RebuildTabs();
        SetActiveTab(tab.Id);
    }

    private void OnSettingsTabRemoved(string tabId)
    {
        if (_tabs.Count <= 1 || string.Equals(tabId, ChatUserSettings.AllTabId, StringComparison.OrdinalIgnoreCase))
            return;

        var removed = _tabs.RemoveAll(tab => tab.Id == tabId) > 0;
        if (!removed)
            return;

        _tabUnread.Remove(tabId);
        if (_activeTabId == tabId)
            _activeTabId = _tabs.FirstOrDefault(IsTabVisible)?.Id ?? _tabs[0].Id;

        if (_secondaryTabId == tabId)
            _secondaryTabId = GetDefaultSecondaryTabId();

        SaveTabs();
        SaveSplitSettings();
        RebuildTabs();
        SetActiveTab(_activeTabId);
    }

    private void OnSettingsTabRenamed(string tabId, string title)
    {
        if (string.Equals(tabId, ChatUserSettings.AllTabId, StringComparison.OrdinalIgnoreCase))
            return;

        var tab = _tabs.FirstOrDefault(entry => string.Equals(entry.Id, tabId, StringComparison.OrdinalIgnoreCase));
        if (tab == null)
            return;

        var sanitized = ChatUserSettings.SanitizeTabTitle(title);
        if (tab.Title == sanitized)
            return;

        tab.Title = sanitized;
        SaveTabs();
        RebuildTabs();
        SyncFilterPopup();
        RefreshVisibleChatLayout();
    }

    private void OnSettingsTabMoved(string tabId, string targetTabId)
    {
        MoveTabByDrag(tabId, targetTabId);
    }

    private void MoveTabByDrag(string tabId, string targetTabId)
    {
        if (string.Equals(tabId, targetTabId, StringComparison.OrdinalIgnoreCase) ||
            string.Equals(tabId, ChatUserSettings.AllTabId, StringComparison.OrdinalIgnoreCase) ||
            string.Equals(targetTabId, ChatUserSettings.AllTabId, StringComparison.OrdinalIgnoreCase))
            return;

        var from = _tabs.FindIndex(tab => string.Equals(tab.Id, tabId, StringComparison.OrdinalIgnoreCase));
        var to = _tabs.FindIndex(tab => string.Equals(tab.Id, targetTabId, StringComparison.OrdinalIgnoreCase));
        if (from <= 0 || to <= 0 || from == to)
            return;

        var moving = _tabs[from];
        _tabs.RemoveAt(from);
        var insertIndex = Math.Clamp(to, 1, _tabs.Count);
        _tabs.Insert(insertIndex, moving);

        SaveTabs();
        RebuildTabs();
        SyncFilterPopup();
        RefreshVisibleChatLayout();
    }

    private void OnSettingsRadioFilter(string label, bool active)
    {
        if (_syncingFilter)
            return;

        if (LegacyPresentation || IsAllTab(GetActiveTab()))
            return;

        var tab = GetActiveTab();
        var normalized = ChatUserSettings.NormalizeRadioLabel(label);
        if (string.IsNullOrWhiteSpace(normalized))
            return;

        if (tab.RadioLabels.Count == 0)
        {
            foreach (var target in _radioTargets)
            {
                if (!tab.RadioLabels.Any(existing => string.Equals(existing, target.Label, StringComparison.OrdinalIgnoreCase)))
                    tab.RadioLabels.Add(target.Label);
            }
        }

        if (active)
        {
            if (!tab.RadioLabels.Any(existing => string.Equals(existing, normalized, StringComparison.OrdinalIgnoreCase)))
                tab.RadioLabels.Add(normalized);
        }
        else
        {
            tab.RadioLabels.RemoveAll(existing => string.Equals(existing, normalized, StringComparison.OrdinalIgnoreCase));
        }

        if (tab.RadioLabels.Count == _radioTargets.Count)
            tab.RadioLabels.Clear();

        SaveTabs();
        SyncFilterPopup();
        Repopulate();
    }

    private void OnSettingsStyleChanged(string target, string? color, string? fontSize)
    {
        if (!_styleTargets.Any(styleTarget => string.Equals(styleTarget.Key, target, StringComparison.OrdinalIgnoreCase)))
            return;

        var normalizedColor = ChatUserSettings.NormalizeColor(color);
        var normalizedFontSize = ChatUserSettings.NormalizeFontSize(fontSize);
        var existing = _styles.FirstOrDefault(style => string.Equals(style.Target, target, StringComparison.OrdinalIgnoreCase));

        if (normalizedColor == null && normalizedFontSize == null)
        {
            if (existing != null)
                _styles.Remove(existing);
        }
        else if (existing == null)
        {
            _styles.Add(new ChatStyleSettings
            {
                Target = target,
                Color = normalizedColor,
                Font = ChatUserSettings.FontDefault,
                FontSize = normalizedFontSize
            });
        }
        else
        {
            existing.Color = normalizedColor;
            existing.Font = ChatUserSettings.FontDefault;
            existing.FontSize = normalizedFontSize;
        }

        SaveStyles();
        SyncFilterPopup();
        Repopulate();
    }

    private void OnSettingsStyleReset(string target)
    {
        _styles.RemoveAll(style => string.Equals(style.Target, target, StringComparison.OrdinalIgnoreCase));
        SaveStyles();
        SyncFilterPopup();
        Repopulate();
    }

