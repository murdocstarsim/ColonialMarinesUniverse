    private void DisplayMessage(ChatMessage msg, bool fromHistory)
    {
        if (LegacyPresentation)
        {
            var visible = IsMessageVisibleInLegacy(msg);
            if (visible)
                AddLegacyLine(msg);

            if (fromHistory || !visible)
                return;

            if (msg is { Read: false, AudioPath: { } })
                _entManager.System<AudioSystem>().PlayGlobal(new ResolvedPathSpecifier(msg.AudioPath), Filter.Local(), false, AudioParams.Default.WithVolume(msg.AudioVolume));

            msg.Read = true;
            return;
        }

        if (fromHistory)
        {
            if (IsMessageVisibleInActiveTab(msg))
                AddLine(msg, Contents, _primaryRepeatQueue);

            if (IsMessageVisibleInSecondaryTab(msg))
                AddLine(msg, SecondaryContents, _secondaryRepeatQueue);

            return;
        }

        if (!IsMessageVisibleInAnyPage(msg))
            return;

        if (msg is { Read: false, AudioPath: { } })
            _entManager.System<AudioSystem>().PlayGlobal(new ResolvedPathSpecifier(msg.AudioPath), Filter.Local(), false, AudioParams.Default.WithVolume(msg.AudioVolume));

        msg.Read = true;

        UpdateInactiveTabUnreads(msg);

        if (IsMessageVisibleInActiveTab(msg))
            AddLine(msg, Contents, _primaryRepeatQueue);

        if (IsMessageVisibleInSecondaryTab(msg))
            AddLine(msg, SecondaryContents, _secondaryRepeatQueue);
    }

