    public void Repopulate()
    {
        if (LegacyPresentation)
        {
            LegacyContents.Clear();
            _legacyRepeatQueue.Clear();

            foreach (var message in _controller.History)
            {
                if (IsMessageVisibleInLegacy(message.Item2))
                    AddLegacyLine(message.Item2);
            }

            LegacyContents.ScrollToBottom();
            return;
        }

        Contents.Clear();
        _primaryRepeatQueue.Clear();

        foreach (var message in _controller.History)
        {
            if (IsMessageVisibleInActiveTab(message.Item2))
                AddLine(message.Item2, Contents, _primaryRepeatQueue);
        }

        Contents.ScrollToBottom();

        if (_splitChatEnabled)
            RepopulateSecondary();
    }

    private void OnChannelFilter(ChatChannel channel, bool active)
    {
        if (_syncingFilter)
            return;

        if (LegacyPresentation)
        {
            _legacyChannelMask = active
                ? _legacyChannelMask | channel
                : _legacyChannelMask & ~channel;

            Repopulate();

            if (active)
                _controller.ClearUnfilteredUnreads(channel);

            return;
        }

        var tab = GetActiveTab();
        if (IsAllTab(tab))
            return;

        tab.ChannelMask = active
            ? tab.ChannelMask | channel
            : tab.ChannelMask & ~channel;
        SaveTabs();

        Repopulate();

        if (active)
            _controller.ClearUnfilteredUnreads(channel);
    }

